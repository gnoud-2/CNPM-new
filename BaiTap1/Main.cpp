// Tệp: main.cpp 
#include <iostream> // Lệnh tiền xử lý để bao gồm thư viện iostream (input/output stream)

// Hàm main - điểm bắt đầu của mọi chương trình C++
int main() {
    // Khai báo một biến kiểu số nguyên (int) và gán giá trị 10
    int soNguyen = 10;

    // Sử dụng std::cout để in một chuỗi và giá trị của biến ra màn hình
    std::cout << "Xin chao, day la chuong trinh C++ co ban!" << std::endl;
    std::cout << "So nguyen la: " << soNguyen << std::endl;

    // Trả về 0 để báo hiệu chương trình thực thi thành công
    return 0;
}
